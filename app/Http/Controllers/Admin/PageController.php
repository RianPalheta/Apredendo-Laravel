    public function get_pages(Request $request) {
        $search['content'] = $request->input('search');

        $qt = intval($request->input('qt', 10));
        $users = Page::where('title', 'like', '%'.$search['content'].'%')
            ->orderByDesc('id')
            ->paginate($qt);

        echo json_encode($users);
        return;
    }

